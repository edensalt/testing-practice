const calculator = {
  add: (inputOne: number, inputTwo: number): number => {
    return inputOne + inputTwo
  },
  subtract: (inputOne: number, inputTwo: number): number => {
    return inputOne - inputTwo
  },
  multiply: (inputOne: number, inputTwo: number): number => {
    return inputOne * inputTwo
  },
  divide: (inputOne: number, inputTwo: number): number => {
    return inputOne / inputTwo
  }
}

export default calculator